package transporte;
public class Aviao extends Veiculo{
    private int turbinas;
    private int capacidade;
    private boolean hidro;
    private boolean retratil;

    public Aviao() {
       super();
    }

    public int getTurbinas() {
        return turbinas;
    }

    public void setTurbinas(int turbinas) {
        this.turbinas = turbinas;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }

    public boolean isHidro() {
        return hidro;
    }

    public void setHidro(boolean hidro) {
        this.hidro = hidro;
    }

    public boolean isRetratil() {
        return retratil;
    }

    public void setRetratil(boolean retratil) {
        this.retratil = retratil;
    }
    
    
    
    
    
    
    
    
}
